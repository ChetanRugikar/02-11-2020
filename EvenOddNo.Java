class EvenOddNo{

	public static void main(String args[]){
	int Array[] = {10,11,22,32,4,7,2,6,17,1};
		
		for(int i=0;i<10;i++){
			if(i%2==0){
				
				System.out.println(Array[i]+" is even number");
	
			}
			else{
				
				System.out.println(Array[i]+" is odd number");
				
			}
				
		}
			
	}

}
